import inspect

def retrieve_variable_name(var):
    """
    Function returns the name of the variable.
    Throws the KeyError exception is name was not found.

    Args:
        var: Variable.

    Returns:
        String representing the variable name.
    """
    for fi in reversed(inspect.stack()):
        names = [var_name for var_name, var_val in fi.frame.f_locals.items() if var_val is var]
        if len(names) > 0:
            return names[0]
        else:
            raise KeyError("Cannot retrieve the name of object {} as it is not present on the stack (yet)".format(var))
